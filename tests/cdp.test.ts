import { describe, it, expect } from 'vitest';
import { ProtocolRegistry } from '../src/core/registry';
import { dissect } from '../src/core/engine';
import { inetChecksum } from '../src/core/checksum';
import { cdp } from '../src/protocols/cdp';

// A REAL CDP v2 capture from a Cisco WS-C4006 switch (Columbia University), as
// published at http://www.cs.columbia.edu/~hgs/internet/cdp.shtml — the CDP
// payload that follows the 802.3 LLC/SNAP header (SNAP OUI 0x00000C, PID 0x2000).
//
// CDP header (4 bytes):
//   0x02        Version = 2
//   0xB4        TTL / hold time = 180 seconds (the Cisco default)
//   0x16 0xCA   Checksum = 0x16CA (Internet checksum over the whole packet)
// Then the TLV stream begins:
//   0x0001 0x001A  Device ID TLV (type 1, length 26 = 4 header + 22 value)
//   "FOX04171416(cepsr-7-1)"  the device id value
//   ... further TLVs (Addresses, Port ID, Capabilities, Software Version,
//   Platform, VTP domain, Native VLAN, Duplex) ...
// The checksum field 0x16CA verifies over all 228 bytes (see the test below).
const cdpPacket = [
  0x02, 0xb4, 0x16, 0xca, 0x00, 0x01, 0x00, 0x1a, 0x46, 0x4f, 0x58, 0x30, 0x34, 0x31, 0x37, 0x31,
  0x34, 0x31, 0x36, 0x28, 0x63, 0x65, 0x70, 0x73, 0x72, 0x2d, 0x37, 0x2d, 0x31, 0x29, 0x00, 0x02,
  0x00, 0x11, 0x00, 0x00, 0x00, 0x01, 0x01, 0x01, 0xcc, 0x00, 0x04, 0x80, 0x3b, 0x10, 0xfa, 0x00,
  0x03, 0x00, 0x07, 0x36, 0x2f, 0x39, 0x00, 0x04, 0x00, 0x08, 0x00, 0x00, 0x00, 0x0a, 0x00, 0x05,
  0x00, 0x6e, 0x57, 0x53, 0x2d, 0x43, 0x34, 0x30, 0x30, 0x36, 0x20, 0x53, 0x6f, 0x66, 0x74, 0x77,
  0x61, 0x72, 0x65, 0x2c, 0x20, 0x56, 0x65, 0x72, 0x73, 0x69, 0x6f, 0x6e, 0x20, 0x4d, 0x63, 0x70,
  0x53, 0x57, 0x3a, 0x20, 0x35, 0x2e, 0x35, 0x28, 0x31, 0x30, 0x2e, 0x30, 0x29, 0x20, 0x4e, 0x6d,
  0x70, 0x53, 0x57, 0x3a, 0x20, 0x35, 0x2e, 0x35, 0x28, 0x31, 0x30, 0x29, 0x0a, 0x43, 0x6f, 0x70,
  0x79, 0x72, 0x69, 0x67, 0x68, 0x74, 0x20, 0x28, 0x63, 0x29, 0x20, 0x31, 0x39, 0x39, 0x35, 0x2d,
  0x32, 0x30, 0x30, 0x31, 0x20, 0x62, 0x79, 0x20, 0x43, 0x69, 0x73, 0x63, 0x6f, 0x20, 0x53, 0x79,
  0x73, 0x74, 0x65, 0x6d, 0x73, 0x2c, 0x20, 0x49, 0x6e, 0x63, 0x2e, 0x0a, 0x00, 0x06, 0x00, 0x0c,
  0x57, 0x53, 0x2d, 0x43, 0x34, 0x30, 0x30, 0x36, 0x00, 0x09, 0x00, 0x17, 0x43, 0x6f, 0x6c, 0x75,
  0x6d, 0x62, 0x69, 0x61, 0x5f, 0x55, 0x6e, 0x69, 0x76, 0x65, 0x72, 0x73, 0x69, 0x74, 0x79, 0x00,
  0x0a, 0x00, 0x06, 0x00, 0x10, 0x00, 0x0b, 0x00, 0x05, 0x01, 0x00, 0x12, 0x00, 0x05, 0x00, 0x00,
  0x13, 0x00, 0x05, 0x00,
];

describe('CDP dissection', () => {
  const reg = new ProtocolRegistry();
  reg.register(cdp);

  it('parses the fixed 4-byte header (version, hold-time, checksum)', () => {
    const node = dissect(cdpPacket, 'cdp', reg);
    const h = node.header;
    expect(h.byteLength).toBe(4);
    expect(h.get('version')).toBe(2);
    expect(h.get('ttl')).toBe(180); // hold time in seconds, the Cisco default
    expect(h.fields.find((f) => f.field.name === 'checksum')!.display).toBe('0x16CA');
  });

  it('leaves the TLV stream in the payload (CDP is the top of this stack)', () => {
    const node = dissect(cdpPacket, 'cdp', reg);
    expect(node.child).toBe(null);
    expect(cdp.next!(node.header, reg)).toBe(null);
    // Everything after the 4-byte header is the TLV stream.
    expect(node.payload.length).toBe(cdpPacket.length - 4);
    // First TLV: type 0x0001 (Device ID), length 0x001A (26 bytes).
    const tlv = node.payload;
    expect((tlv[0] << 8) | tlv[1]).toBe(0x0001); // Device ID
    expect((tlv[2] << 8) | tlv[3]).toBe(0x001a); // 26-byte TLV
    // The 22-byte value decodes to the device id string.
    const value = String.fromCharCode(...tlv.slice(4, 26));
    expect(value).toBe('FOX04171416(cepsr-7-1)');
  });

  it('checksum is the RFC 1071 Internet checksum over the entire CDP packet', () => {
    // Recompute over the full packet (228 bytes) with the checksum field zeroed;
    // the result must equal the 0x16CA carried on the wire. This anchors the
    // capture to the algorithm, not to the spec's own output.
    const zeroed = cdpPacket.slice();
    zeroed[2] = 0;
    zeroed[3] = 0;
    expect(inetChecksum(zeroed)).toBe(0x16ca);
  });
});
